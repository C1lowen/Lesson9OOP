package lesson9_task3;

public class LetterReadFromFile {

   private char character;
   private int number;

    public LetterReadFromFile(char character, int number) {
        this.character = character;
        this.number = number;
    }

    public char getCharacter() {
        return character;
    }

    public int getNumber() {
        return number;
    }
}
